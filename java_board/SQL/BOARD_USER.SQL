-- 유저 보드 생성
CREATE TABLE BOARD_USER(
  NO NUMBER(20,0) NOT NULL, 
	TID VARCHAR2(100) NOT NULL UNIQUE, 
	TPW VARCHAR2(100) NOT NULL, 
	NAME VARCHAR2(255) DEFAULT '이름미입력', 
	AGE NUMBER(20) DEFAULT 0,
  PRIMARY KEY(NO)
);

-- NO에 자동으로 입력될 유저 시퀀스 생성
CREATE SEQUENCE SEQ_USER 
INCREMENT BY 1 
START WITH 1 
MINVALUE 1 
MAXVALUE 9999;

-- 테스트 데이터
Insert into BOARD_USER (NO,TPW,TID,NAME,AGE) values (SEQ_USER.NEXTVAL,'12345','ID','ex',20);
Insert into BOARD_USER (NO,TPW,TID,NAME,AGE) values (SEQ_USER.NEXTVAL,'JavaFx','hello','DB',null);
Insert into BOARD_USER (NO,TPW,TID,NAME,AGE) values (SEQ_USER.NEXTVAL,'javaFx','Hello','dB',null);


